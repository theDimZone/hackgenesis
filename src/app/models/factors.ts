export class Factors {
  // factors are from 0 to 100 (in percents)
  public risky: number = 0; // рискованность
  public diversification: number = 0; // желание диверсифицироваться
  public duration: number = 0; // предпочтение на сроки вложения
  public boomer: number = 0; // насколько предпочтительны обще-известные бренды
  public skill: number = 0; // осведомленность в сфере инвестирования
  public optimism: number = 0; // оптимизм пользователя
}
